    private void btCarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCarregarActionPerformed
        // TODO add your handling code here:
        lbMsg.setText("Abrindo imagem.....");
        this.setVisible(true);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("BMP","bmp","arquivo bmp");
        if (fcBuffer==null)
        {
            fcBuffer = new JFileChooser();
        }
        fcBuffer.setFileFilter(filter);
        
        int returnVal = fcBuffer.showOpenDialog(this);
        
        try{
            if (returnVal == JFileChooser.APPROVE_OPTION)
            {
                File file = fcBuffer.getSelectedFile();
                fileBuffer = file;
                String nomeCompleto = file.getName();
                String basicoNome = nomeCompleto.substring(0,9);
                String extensao = nomeCompleto.substring(nomeCompleto.length()-4, nomeCompleto.length());
                int id = 0;
                File dir = file.getParentFile();
                while (true){ // Procura para ver quantos arquivos tem
                    String nomeProcurado = dir.getPath()+"//"+basicoNome+util.retornarStringPorNumero(id)+extensao;
                    File proximaFile = new File(nomeProcurado);
                    if (proximaFile.exists()){
                        id = id+1;
                    }else{
                        id = id-1;
                        break;
                    }
                }
                // (File diretorio,String baseNome,String extensao,int ultimoID,File nameOut)
                File fileOut = new File(dir,"comprimido.hue");
                VideoWriter.write(dir, basicoNome, extensao, id, fileOut);
                /*
                JFrame frame = new JFrame();
                imagemPanel imgP = new imagemPanel(file);
                frame.add(imgP);
                frame.pack();
                frame.setVisible(true);
                */
            }
            else
            {
                lbMsg.setText("Cancelado!");
                //this.setVisible(true);
            }
        }
        catch( Exception e)
        {
            //this.setVisible(true);
            e.printStackTrace();
            lbMsg.setText("Erro!");
            taAvisos.setText("Erro e = " + e.getMessage());
        }
        System.out.println("end");
    }//GEN-LAST:event_btCarregarActionPerformed

