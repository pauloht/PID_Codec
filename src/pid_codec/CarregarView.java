    private void btAbrirVideoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAbrirVideoActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        lbMsg.setText("Abrindo video..");
        this.setVisible(true);
        FileNameExtensionFilter filter = new FileNameExtensionFilter("HUE","hue","arquivo hue");
        if (fcBuffer==null)
        {
            fcBuffer = new JFileChooser();
        }
        fcBuffer.setFileFilter(filter);
        
        int returnVal = fcBuffer.showOpenDialog(this);
        
        try{
            if (returnVal == JFileChooser.APPROVE_OPTION)
            {
                File file = fcBuffer.getSelectedFile();
                VideoReader vr = new VideoReader(file);
                
                
                Timer timer;
                tempos[0] = System.nanoTime();
                ActionListener listener = new ActionListener(){
                    @Override
                    public void actionPerformed(ActionEvent event){
                        //System.out.println("next frame!");
                        PTimer tempo = new PTimer("tempo exibicao");
                        tempo.startTimer();
                        Timer t = (Timer)event.getSource();
                        boolean ret = vr.nextFrame();
                        tempo.endTimer();
                        long elapsed = tempo.getElapsed();
                        if (ret){
                            historicoDeTempos.add(elapsed);
                        }
                        if (!ret  || elapsed > 33333333){
                            t.restart();
                            //System.out.println("estouro");
                        }else{
                            t.restart();
                            //int delay = (int) ((33333333-elapsed)/1E6);
                            //t.setDelay(delay);
                        }
                        if (vr.hasVideoEnded()){
                            tempos[1] = System.nanoTime();
                            for (Long l : historicoDeTempos){
                                System.out.println("Tempo : " + (l/1E9+0.00) + "s");
                            }
                            System.out.println("Tempo total : " + ((tempos[1]-tempos[0])/(1E9+0.00)));
                            ((Timer)event.getSource()).stop();
                        }
                        //System.out.println("tempo :  " + tempo);
                    }
                };
                
                timer = new Timer(33, listener);
                timer.setRepeats(true);
                timer.start();
                
                //t1.run();
                /*
                JFrame frame = new JFrame();
                imagemPanel imgP = new imagemPanel(file);
                frame.add(imgP);
                frame.pack();
                frame.setVisible(true);
                */
            }
            else
            {
                lbMsg.setText("Cancelado!");
                //this.setVisible(true);
            }
        }
        catch( Exception e)
        {
            //this.setVisible(true);
            e.printStackTrace();
            lbMsg.setText("Erro!");
            taAvisos.setText("Erro e = " + e.getMessage());
        }
        System.out.println("end");
    }//GEN-LAST:event_btAbrirVideoActionPerformed
